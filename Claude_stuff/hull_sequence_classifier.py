"""
hull_sequence_classifier.py
============================
Transformer-based sequential hull classifier.

Replaces the MLP in LearnedHullClassifier with a causal Transformer
that processes the full trajectory history before labeling each step.

Key design decisions:
  - Input:   [state | action | delta_state] per step  → rich trajectory encoding
  - Model:   Causal Transformer (GPT-style masking) so step t only sees t' <= t
  - Output:  Per-step soft probability + epistemic uncertainty (MC Dropout)
  - Labels:  Generated by any exact oracle (PostEpisodeHullMonitor, etc.)
  - Training: Online — label buffer fills as episodes complete, model retrains
              periodically on batched trajectory windows

Interface: identical to LearnedHullClassifier — drop-in replacement.
    monitor = SequentialHullClassifier(xDim, uDim, action_bounds)
    result  = monitor.process(record)   # HullResult with soft probs + uncertainty
"""

